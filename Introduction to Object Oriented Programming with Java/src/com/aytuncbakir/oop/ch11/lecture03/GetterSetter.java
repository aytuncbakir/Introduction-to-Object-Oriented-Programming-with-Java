package com.aytuncbakir.oop.ch11.lecture03;

public class GetterSetter {
	
	private int a;
	private String s;
	private boolean b;
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	
	public boolean isB() {     // boolean getter
		return b;
	}
	public void setB(boolean b) {
		this.b = b;
	}
	
}
